Tipos de bases de datos NoSQL

Bases de datos documentales:
Almacena la informacion en documentos con formato JSON o similar, permitiendo
estructuras flexibles y datos anidados

Bases de datos clave-valor:
Guarda informacion como pared de vlave y valor, permitiendo acceso
rapido a los datos mediante una clave unica.

Bases de datos orientadas a columnas:
Organiza los datos por columnas en lugar de filas, optimizando el manejo de grandes volumenes
de informacion.

Bases de datos de grafos:
Representa la informacion mediante nodos y relaciones, ideal para analizar conexiones
entre datos.

Conceptos de interes

Documento(en NoSQL):
Unidad basica de almacenamiento en una base datos documental, que contiene datos en formato JSON
con estructura flexible.

Coleccion:
Conjunto de documentos dentro de una base de datos NoSQL, similar a una
tabla en bases de datos relacionales.

Campo:
Elemnto individual dentro de un documento, equivalente a un atributo o columna.

Subdocumento:
Documento que se encuentra dentro de otro documento, permitiendo representar estructuras complejas

Arreglo (Array)
Lista de valores dentro de un documento, que puede contener datos simples o subdocumentos.

Bson:
Formato binario de JSON utilizado por MongoDB para almacenar datos de manera mas eficiente

MongoDB
Sistema de bases de datos NoSQL documental que almacena datos en formato JSON (BSON), ampliamente utilizado
en aplicaciones modernas.

*/

